class beehive:
    # Constants 
    number_of_beehives = 1

    def __init__(self, bees, queen, drones):
        self.bees = bees
        self.queen = queen
        self.drones = drones 
    # Methods
    def growth():
        if self.queen != 1:
            self.bees == self.bees
        elif self.queen == 1 and self.drones >= 5:
            self.bees == self.bees + 1
    
my_beehive = beehive("Beehive 1", "20", "1", "5")
my_beehive.growth()